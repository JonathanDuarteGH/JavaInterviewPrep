// Java End-of-File


import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int i = 1;
        while (sc.hasNextLine()) {
            String scan = sc.nextLine();
            System.out.println(i + " " + scan);
            i++;
      }       
    }
}